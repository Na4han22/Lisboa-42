#include <unistd.h>

void	ft_countdown(void)
{
	char	c;

	c = 57;
	while (c >= 48)
	{
		write(1, &c, 1);
		write(1, "\n", 2);
		c--;
	}
}

int	main(void)
{
	ft_countdown();
	return (0);
}
