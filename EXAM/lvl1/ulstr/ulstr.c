#include <unistd.h>

int	main(int argc, char *argv[])
{
	int		i;
	char	l;

	i = 0;
	if (argc == 2)
	{
		while (argv[1][i])
		{
			l = argv[1][i];
			if (argv[1][i] >= 65 && argv[1][i] <= 90)
				l += 32;
			if (argv[1][i] >= 97 && argv[1][i] <= 122)
				l -= 32;
			write (1, &l, 1);
			i += 1;
		}
	}
	write (1, "\n", 1);
	return (0);
}
