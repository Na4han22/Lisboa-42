#include <unistd.h>

int	main(int argc, char **argv)
{
	int		i;
	char	l;

	i = 0;
	if (argc == 2)
	{
		while (argv[1][i])
		{
			l = argv[1][i];
			if (65 <= l && 90 >= l)
				l = 90 - l + 65;
			if (97 <= l && 122 >= l)
				l = 122 - l + 97;
			write (1, &l, 1);
			i++;
		}
	}
	write (1, "\n", 1);
	return (0);
}
